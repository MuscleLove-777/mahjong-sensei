"""麻雀戦術ラボ - プロンプト定義

麻雀特化ブログ用のプロンプトを一元管理する。
データ・統計ベースの戦術解説、牌姿のテキスト表現を重視。
"""


def build_keyword_prompt(config):
    """麻雀特化キーワード選定プロンプトを構築する"""
    categories_text = "\n".join(f"- {cat}" for cat in config.TARGET_CATEGORIES)
    return (
        "麻雀戦術の専門ブログ用のキーワードを選定してください。\n\n"
        "以下のカテゴリから1つ選び、そのカテゴリで検索需要が高い"
        "麻雀関連のキーワードを1つ提案してください。\n\n"
        f"カテゴリ一覧:\n{categories_text}\n\n"
        "【キーワード選定の基準】\n"
        "- ロングテールキーワード（3語以上の組み合わせ）を優先\n"
        "- 具体的な場面・状況に紐づくキーワードを選ぶ\n"
        "- 「麻雀 ○○ コツ」「○○ 切り方」「Mリーグ ○○」などの実用系\n"
        "- 「リーチ判断 ○○」「副露 ○○」「牌効率 ○○」などの戦術系\n"
        "- 「点数計算 ○○」「符計算 ○○」などの計算系\n"
        "- 季節・イベント性も考慮（Mリーグシーズン、大会時期など）\n\n"
        "以下の形式でJSON形式のみで回答してください（説明不要）:\n"
        '{"category": "カテゴリ名", "keyword": "キーワード"}'
    )


def build_article_prompt(keyword, category, config):
    """麻雀戦術ラボ記事生成プロンプトを構築する"""
    return f"""あなたは麻雀データアナリスト兼プロ雀士レベルの解説者です。
統計データに基づいた戦術解説を得意とし、初心者にもわかりやすく、
上級者にも新しい発見がある記事を書きます。

以下のキーワードについて、データと理論に基づく戦術記事を書いてください。

【基本条件】
- ブログ名: {config.BLOG_NAME}
- キーワード: {keyword}
- カテゴリ: {category}
- 言語: 日本語
- 文字数: {config.MAX_ARTICLE_LENGTH}文字程度

【記事フォーマット】
## この記事の結論
- 結論1（最も重要なポイント）
- 結論2
- 結論3

## 基本理論
（なぜこの戦術が有効なのか、期待値ベースで解説。
可能であれば受入枚数・和了率・放銃率などの数値を用いて根拠を示す）

## 具体的な牌姿・場面例
（テキストで牌姿を表現する。以下の表記ルールに従うこと：
- 萬子: 一二三四五六七八九
- 筒子: (1)(2)(3)(4)(5)(6)(7)(8)(9) または丸数字 ①②③④⑤⑥⑦⑧⑨
- 索子: 1s2s3s...9s または 123456789（アラビア数字）
- 字牌: 東南西北白發中
- 例: 一二三④⑤⑥789東東南南南
各牌姿について「何を切るべきか」「なぜそう切るのか」を受入枚数とともに解説）

## 実戦での応用パターン
（場況別の判断基準を整理する：
- 東場 vs 南場での違い
- 点数状況による判断の変化
- 他家の捨て牌・副露状況からの読み
- 巡目による判断基準の変化）

## よくある間違い・罠
（初中級者が陥りやすいミスパターンを具体例とともに解説。
「こう打ちたくなるが、実はこちらが正着」という形で示す）

## まとめ：こんな場面で使おう
（この戦術が特に有効な場面を箇条書きで整理。
実戦で判断に迷ったときのチェックリストとして使える形にする）

【SEO要件】
1. タイトルにキーワード「{keyword}」を必ず含めること
2. タイトルは32文字以内で魅力的に
3. H2、H3の見出し構造を適切に使用すること
4. キーワード密度は{config.MIN_KEYWORD_DENSITY}%〜{config.MAX_KEYWORD_DENSITY}%を目安に
5. メタディスクリプションは{config.META_DESCRIPTION_LENGTH}文字以内

【差別化ポイント】
- データ・統計に基づく解説を必ず含める（和了率、放銃率、受入枚数など）
- Mリーグや公式戦の実例を引用する場合は選手名・対局名を明記
- 牌姿は必ずテキストで表現し、読者がイメージしやすいようにする
- 「感覚」ではなく「数値」で説明する姿勢を貫く
- 内部リンクのプレースホルダーを2〜3箇所に配置（{{{{internal_link:関連トピック}}}}の形式）

【出力形式】
以下のJSON形式で出力してください。JSONブロック以外のテキストは出力しないでください。

```json
{{
  "title": "SEO最適化されたタイトル",
  "content": "# タイトル\\n\\n本文（Markdown形式）...",
  "meta_description": "120文字以内のメタディスクリプション",
  "tags": ["タグ1", "タグ2", "タグ3", "タグ4", "タグ5"],
  "slug": "url-friendly-slug"
}}
```

【注意事項】
- content内のMarkdownは適切にエスケープしてJSON文字列として有効にすること
- tagsは5個ちょうど生成すること
- slugは半角英数字とハイフンのみ使用すること
- 麻雀用語には初出時に（カッコ）で平易な補足説明を付けること
- 数値やデータの根拠を可能な限り示すこと"""
